<?php 
//do ... while
$y = 10;
do {
      echo "test <br>";
      $y--;
} while ($y > 0);

//menggunakan while
$x = 10;

while ($x  == 0) {
      echo "variable x lebih besar dari 0 <br>";
      $x--;
}

// menggunakan for loop
//mencetak angka 1 - 10
for ($i = 1; $i < 10; $i++) { 
      echo "$i";
      echo "<br>";
}
echo "test";
?>